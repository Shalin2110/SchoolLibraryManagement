    public boolean valIssue(){
        String BookId = txtBookId.getText();
        String StId = txtStudentId.getText();
        
        if (BookId.equals("") || !BookId.matches("^B\\d{3}$")){
            JOptionPane.showMessageDialog(this, "Please Enter Valid Book Id");
            return false;
        }
        if (StId.equals("") || !StId.matches("^S\\d{3}$")){
            JOptionPane.showMessageDialog(this, "Please Enter Valid User Id");
            return false;
        }
        return true;
    }

