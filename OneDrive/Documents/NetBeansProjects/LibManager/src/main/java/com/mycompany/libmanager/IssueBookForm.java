    private void btnIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueActionPerformed
        if(valIssue()){
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String BookId = txtBookId.getText();
            String StId = txtStudentId.getText();
            String IssueDate = sdf.format(dateChooseIssue.getDate());
            String DueDate = sdf.format(dateChooseDue.getDate());
            String Status = "Issued";

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarymanagement","root","21101105");
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("SELECT * FROM tblbook WHERE BookId = '"+BookId+"'"); 
                if(rs.next()){
                    ResultSet rs1 = st.executeQuery("SELECT * FROM tblstudent WHERE StudentId = '"+StId+"'");
                    if(rs1.next()){
                        st.executeUpdate("INSERT INTO issuebook VALUES('"+BookId+"','"+StId+"','"+IssueDate+"','"+DueDate+"','"+Status+"')");
                        JOptionPane.showMessageDialog(this, "Successfully issued book!");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Invalid student id!");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Invalid book id!");
                }
                con.close();
            } catch (ClassNotFoundException | SQLException e) {
                    System.out.println(e);
            }
        }
    }//GEN-LAST:event_btnIssueActionPerformed

