    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel(); 
        int selectedRowIndex = jTable1.getSelectedRow(); 
        String id = (String) dtm.getValueAt(selectedRowIndex, 0);
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarymanagement","root","21101105");
            //Connection con = DBConnection.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tblbook WHERE BookId = '"+id+"'"); 
            while(rs.next()){
                txtBookId.setText(rs.getString("BookId"));
                txtTitle.setText(rs.getString("Title"));
                txtAuthor.setText(rs.getString("Author"));
                txtQty.setText(rs.getString("Quantity"));
            }
            con.close();
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

