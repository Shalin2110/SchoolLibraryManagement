    public boolean valBook(){
        String BookId = txtBookId.getText();
        String Title = txtTitle.getText();
        String Author = txtAuthor.getText();
        String QtyStr = txtQty.getText();
        
        if (BookId.equals("") || !BookId.matches("^B\\d{3}$")){
            JOptionPane.showMessageDialog(this, "Please Enter Valid Book Id");
            return false;
        }
        if (Title.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Book Title");
            return false;
        }
        if (Author.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Book Author");
            return false;
        }
        if (QtyStr.isEmpty() || !QtyStr.matches("^\\d+$")){ 
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Quantity");
            return false;
        }
        int Qty = Integer.parseInt(txtQty.getText());
        if (Qty <= 0){
            JOptionPane.showMessageDialog(this, "Quantity must be greater than 0");
            return false;
        }
        return true;
    }

