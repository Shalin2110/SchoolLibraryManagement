    private void btnSave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave2ActionPerformed
        if(valBook()){
            String BookId = txtBookId.getText();
            String Title = txtTitle.getText();
            String Author = txtAuthor.getText();
            int Qty = Integer.parseInt(txtQty.getText());

            try{
                Connection con = DBConnection.getCon();
                Statement st = con.createStatement();
                st.executeUpdate("INSERT INTO tblbook VALUES('"+BookId+"','"+Title+"','"+Author+"','"+Qty+"')");
                JOptionPane.showMessageDialog(this, "Successfully Added!");
                loadBook();

                con.close();
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Book Already Exists!");
            }
        }
    }//GEN-LAST:event_btnSave2ActionPerformed

    private void btnUpdate2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate2ActionPerformed
        if(valBook()){
            String BookId = txtBookId.getText();
            String Title = txtTitle.getText();
            String Author = txtAuthor.getText();
            int Qty = Integer.parseInt(txtQty.getText());

            try{
                Connection con = DBConnection.getCon();
                Statement st = con.createStatement();
                st.executeUpdate("UPDATE tblbook SET Title = '"+Title+"', Author = '"+Author+"', Quantity = '"+Qty+"' WHERE BookId = '"+BookId+"'");
                JOptionPane.showMessageDialog(this, "Successfully Updated!");
                loadBook();

                con.close();
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Failed to update!");
            }
        }
    }//GEN-LAST:event_btnUpdate2ActionPerformed

